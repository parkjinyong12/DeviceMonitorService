package com.ruokit.device.monitor.model.view.code;

public class CodeDetail {
    private String cdId;
    private String cd;
    private String cdNm;
    private String cdDc;
    private String upperCdId;

    public String getCdId() {
        return cdId;
    }

    public void setCdId(String cdId) {
        this.cdId = cdId;
    }

    public String getCd() {
        return cd;
    }

    public void setCd(String cd) {
        this.cd = cd;
    }

    public String getCdNm() {
        return cdNm;
    }

    public void setCdNm(String cdNm) {
        this.cdNm = cdNm;
    }

    public String getCdDc() {
        return cdDc;
    }

    public void setCdDc(String cdDc) {
        this.cdDc = cdDc;
    }

    public String getUpperCdId() {
        return upperCdId;
    }

    public void setUpperCdId(String upperCdId) {
        this.upperCdId = upperCdId;
    }
}
