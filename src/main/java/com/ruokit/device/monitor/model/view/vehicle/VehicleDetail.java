package com.ruokit.device.monitor.model.view.vehicle;

public class VehicleDetail {
    private String vehicleId;
    private String vehicleTp;
    private String vehicleTpNm;
    private String vehicleNum;
    private String cmpnId;
    private String cmpnNm;
    private String dvcId;

    public String getVehicleId() {
        return vehicleId;
    }

    public void setVehicleId(String vehicleId) {
        this.vehicleId = vehicleId;
    }

    public String getVehicleTp() {
        return vehicleTp;
    }

    public void setVehicleTp(String vehicleTp) {
        this.vehicleTp = vehicleTp;
    }

    public String getVehicleTpNm() {
        return vehicleTpNm;
    }

    public void setVehicleTpNm(String vehicleTpNm) {
        this.vehicleTpNm = vehicleTpNm;
    }

    public String getVehicleNum() {
        return vehicleNum;
    }

    public void setVehicleNum(String vehicleNum) {
        this.vehicleNum = vehicleNum;
    }

    public String getCmpnId() {
        return cmpnId;
    }

    public void setCmpnId(String cmpnId) {
        this.cmpnId = cmpnId;
    }

    public String getCmpnNm() {
        return cmpnNm;
    }

    public void setCmpnNm(String cmpnNm) {
        this.cmpnNm = cmpnNm;
    }

    public String getDvcId() {
        return dvcId;
    }

    public void setDvcId(String dvcId) {
        this.dvcId = dvcId;
    }
}
